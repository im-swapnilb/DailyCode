public class Swap_Numbers {

	   public static void main(String[] args) {
	   
		   int a= 100;
		   int b =50;
		   System.out.println("a before is :" +a + " b before is " + b);
		   a = a+b;
		   b=a-b;
		   a= a-b;
		   
		   System.out.println("a is :" +a + " b is " + b);
   }
}
	

